    private void salvarCartaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarCartaActionPerformed
        // TODO add your handling code here:
        if((NcompletoCarta.getText().equals(""))||(BICarta.getText().equals("")))
        {
            JOptionPane.showMessageDialog(null, "Preencha todos os Campos");
        }

        else if(dataEmissaoCarta.getDate() == null||dataExpiraCarta.getDate() == null)
        {
             JOptionPane.showMessageDialog(null, "datas Invalidas!");
        }
        
        else{
            if(!"BI".equals(txtbi.getText()))
            {
                
            String NomeCompleto = NcompletoCarta.getText();
            String OBI = BICarta.getText();
            SimpleDateFormat ft = new SimpleDateFormat("yyyy-MM-dd");
            String dataEmissao = ft.format(dataEmissaoCarta.getDate());
            String dataExpirar = ft.format(dataExpiraCarta.getDate());
            salvarCarta.setEnabled(false);
            OpSelected.setEnabled(false);
            limparcarta.setEnabled(false);
            JOptionPane.showMessageDialog(null, "DADOS ADICIONADOS COM SUCESSO, PODE AVAN√áAR");
        nextStep.setEnabled(true);
                provaDeVida pv = new provaDeVida(NomeCompleto, OBI, dataEmissao, dataExpirar, txtbi.getText());
                 try {
                BufferedWriter conexao = new BufferedWriter(new FileWriter("provas_de_vida.txt", true));
                provaDeVidaControle pvc = new provaDeVidaControle();
                pvc.Salvar(conexao, pv);
                    } catch (IOException ex) {
                        Logger.getLogger(DadosFinanceiros.class.getName()).log(Level.SEVERE, null, ex);
                    }
        
            }
            
            else{            
                JOptionPane.showMessageDialog(null, "Sem Utente Selecionado");
            }
        }
    }//GEN-LAST:event_salvarCartaActionPerformed

